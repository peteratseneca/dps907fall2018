        // GET api/Account/ExternalLogins?returnUrl=%2F&generateState=true
        /// <summary>
        /// List of external logins for the current authenticated user
        /// </summary>
        /// <param name="returnUrl">Return URL</param>
        /// <param name="generateState">True or false</param>
        /// <returns>Detailed data</returns>
        [AllowAnonymous]
        [Route("ExternalLogins")]
        public IEnumerable<ExternalLoginViewModel> GetExternalLogins(string returnUrl, bool generateState = false)
        {
            IEnumerable<AuthenticationDescription> descriptions = Authentication.GetExternalAuthenticationTypes();
            List<ExternalLoginViewModel> logins = new List<ExternalLoginViewModel>();

            string state;

            if (generateState)
            {
                const int strengthInBits = 256;
                state = RandomOAuthStateGenerator.Generate(strengthInBits);
            }
            else
            {
                state = null;
            }

            foreach (AuthenticationDescription description in descriptions)
            {
                ExternalLoginViewModel login = new ExternalLoginViewModel
                {
                    Name = description.Caption,
                    Url = Url.Route("ExternalLogin", new
                    {
                        provider = description.AuthenticationType,
                        response_type = "token",
                        client_id = Startup.PublicClientId,
                        redirect_uri = new Uri(Request.RequestUri, returnUrl).AbsoluteUri,
                        state = state
                    }),
                    State = state
                };
                logins.Add(login);
            }

            return logins;
        }

        // POST api/Account/Register
        /// <summary>
        /// Register a new user account
        /// </summary>
        /// <param name="model">Data package that includes an identifier, password, etc.</param>
        /// <returns>HTTP 200 or an error response</returns>
        [AllowAnonymous]
        [Route("Register")]
        public async Task<IHttpActionResult> Register(RegisterBindingModel model)
        {
            if (!ModelState.IsValid)
            {
                return BadRequest(ModelState);
            }

            // Before attempting to add the user, let's validate the claims

            // Reference to the manager
            Manager m = new Manager();

            // Status field to indicate whether the requested claims are allowed
            // The first "not found" or mal-formed claim will change it to "false"
            bool canRegister = true;

            // Look at the role claims
            foreach (var roleClaimValue in model.Roles)
            {
                if (m.AppClaimGetByMatch("role", roleClaimValue.Trim().ToLower()) == null)
                {
                    canRegister = false;
                }
            }

            // Look at the custom claims
            foreach (var customClaim in model.CustomClaims)
            {
                // Each custom claim is in the format Type=Value
                // So, we'll use string-split to help us with this task
                var claim = customClaim.Split('=');

                // Make sure we can continue
                if (claim.Length == 2)
                {
                    var claimType = claim[0].Trim();
                    var claimValue = claim[1].Trim();

                    // Now, attempt to find a matching claim (similar to above, in the role claim code block)
                    if (m.AppClaimGetByMatch(claimType, claimValue) == null)
                    {
                        canRegister = false;
                    }
                }
                else
                {
                    canRegister = false;
                }
            }

            if (canRegister)
            {
                var user = new ApplicationUser() { UserName = model.Email, Email = model.Email };

                IdentityResult result = await UserManager.CreateAsync(user, model.Password);

                if (result.Succeeded)
                {
                    // Add the new claims that were submitted by the user/requestor

                    await UserManager.AddClaimAsync(user.Id, new Claim(ClaimTypes.Email, model.Email));
                    await UserManager.AddClaimAsync(user.Id, new Claim(ClaimTypes.Role, "User"));
                    await UserManager.AddClaimAsync(user.Id, new Claim(ClaimTypes.GivenName, model.GivenName));
                    await UserManager.AddClaimAsync(user.Id, new Claim(ClaimTypes.Surname, model.Surname));

                    foreach (var role in model.Roles)
                    {
                        await UserManager.AddClaimAsync(user.Id, new Claim(ClaimTypes.Role, role));
                    }

                    // Add custom claims that were submitted by the user/requestor
                    // Above, in the validation block of code, we determined that the custom claims were valid
                    // So, here, we just add them
                    foreach (var customClaim in model.CustomClaims)
                    {
                        var claim = customClaim.Split('=');
                        var claimType = claim[0].Trim();
                        var claimValue = claim[1].Trim();

                        // Then, add...
                        await UserManager.AddClaimAsync(user.Id, new Claim(claimType, claimValue));
                    }
                }
                else
                {
                    return GetErrorResult(result);
                }
                return Ok();
            }
            else
            {
                return StatusCode(HttpStatusCode.BadRequest);
                // We should give the user more useful info
            }
        }

